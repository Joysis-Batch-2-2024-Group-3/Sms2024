package Model;

public class Schedule {
    private int schedID;
    private String shift;

    public int getSchedID() {
        return schedID;
    }

    public String getShift() {
        return shift;
    }

    public void setSchedID(int schedID) {
        this.schedID = schedID;
    }

    public void setShift(String shift) {
        this.shift = shift;
    }
}
